/* ------------------------------- pagination ------------------------------- */
const ITEMS_PER_PAGE = 3;

export function getTotalPages(data) {
  
  const totalPages = Math.ceil(data.length / ITEMS_PER_PAGE);
  return totalPages;
}

export function getDataByPageAndCategory(page, category) {
  let data;
  if (category === 'all') {
    data = beverages;
  } else {
    data = beverages.filter(beverage => beverage.category === category);
  }
  const offset = (page - 1) * ITEMS_PER_PAGE;
  return { partition: data.slice(offset, offset + ITEMS_PER_PAGE), length: getTotalPages(data) };
}


